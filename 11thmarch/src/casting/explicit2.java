package casting;

public class explicit2 {

	public static void main(String [] args) {
	//explicit casting 
	
	float f1 = 45.52f;
	
    System.out.println(f1);
    
    int num = (int) f1;
    
    System.out.println(num);
    
    
    long l1 = 1456245654882l;
    System.out.println(l1);
    
    int num2= (int) l1;
    
    System.out.println(num2);

	}
	
}
